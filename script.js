// Настройки теста
let currentQuestionIndex = 0;
let score = 0;
let flaggedQuestions = new Set();
let timeLeft = 9000; // 150 минут в секундах
let timer;
let answerSelected = false;

// Фильтруем массив, чтобы убрать пустые вопросы (только с номером)
const filteredQuizData = quizData.filter(item => item.options.length > 0);

function startQuiz() {
    document.getElementById('total-questions').textContent = filteredQuizData.length;
    shuffleQuestions();
    showQuestion();
    startTimer();
}

function shuffleQuestions() {
    for (let i = filteredQuizData.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [filteredQuizData[i], filteredQuizData[j]] = [filteredQuizData[j], filteredQuizData[i]];
    }
}

function showQuestion() {
    answerSelected = false;
    const questionContainer = document.getElementById('question-container');
    const currentQuestion = filteredQuizData[currentQuestionIndex];

    // Находим оригинальный номер вопроса из полного массива
    const originalIndex = quizData.findIndex(item => 
        item.question === currentQuestion.question && 
        item.options.length > 0
    );
    const questionNumber = quizData[originalIndex - 1]?.question || "1";

    questionContainer.innerHTML = `
        <div class="question-number">Вопрос ${questionNumber}</div>
        <div class="question-text">${currentQuestion.question}</div>
        ${currentQuestion.options.map((option, index) => `
            <label class="answer">
                <input type="radio" name="answer" value="${index}">
                ${option}
            </label>
        `).join('')}
    `;
    updateFlagButton();
    document.getElementById('next-btn').disabled = true;
}

function updateFlagButton() {
    const flagBtn = document.getElementById('flag-btn');
    const currentOriginalIndex = getOriginalIndex(currentQuestionIndex);
    
    if (flaggedQuestions.has(currentOriginalIndex)) {
        flagBtn.innerHTML = "⚑ Убрать метку";
        flagBtn.style.backgroundColor = "#F57C00";
    } else {
        flagBtn.innerHTML = "⚑ Пометить";
        flagBtn.style.backgroundColor = "#FF9800";
    }
    document.getElementById('flagged-count').textContent = flaggedQuestions.size;
}

function getOriginalIndex(filteredIndex) {
    const currentQuestion = filteredQuizData[filteredIndex];
    return quizData.findIndex(item => 
        item.question === currentQuestion.question && 
        item.options.length > 0
    );
}

function startTimer() {
    timer = setInterval(() => {
        timeLeft--;
        
        const hours = Math.floor(timeLeft / 3600);
        const minutes = Math.floor((timeLeft % 3600) / 60);
        const seconds = timeLeft % 60;
        
        document.getElementById('time').textContent = 
            `${hours.toString().padStart(2, '0')}:${minutes.toString().padStart(2, '0')}:${seconds.toString().padStart(2, '0')}`;

        if (timeLeft <= 0) endQuiz();
    }, 1000);
}

function checkAnswer(selectedIndex) {
    if (answerSelected) return;
    answerSelected = true;
    
    const currentQuestion = filteredQuizData[currentQuestionIndex];
    const isCorrect = selectedIndex == currentQuestion.correctAnswer;

    if (isCorrect) {
        score++;
        document.getElementById('score').textContent = score;
    }

    const answers = document.querySelectorAll('.answer');
    answers.forEach((answer, index) => {
        const radio = answer.querySelector('input[type="radio"]');
        radio.disabled = true;
        
        if (index == currentQuestion.correctAnswer) {
            answer.classList.add('correct');
        } else if (index == selectedIndex) {
            answer.classList.add('incorrect');
        }
    });
    
    document.getElementById('next-btn').disabled = false;
}

function endQuiz() {
    clearInterval(timer);
    document.getElementById('question-container').innerHTML = `
        <h2>Тест завершён!</h2>
        <p>Ваш результат: <strong>${score}</strong> из <strong>${filteredQuizData.length}</strong></p>
        <p>Помечено вопросов: <strong>${flaggedQuestions.size}</strong></p>
    `;
    document.querySelector('.controls').style.display = 'none';
}

// Обработчики событий
document.getElementById('flag-btn').addEventListener('click', () => {
    const originalIndex = getOriginalIndex(currentQuestionIndex);
    flaggedQuestions.has(originalIndex) 
        ? flaggedQuestions.delete(originalIndex) 
        : flaggedQuestions.add(originalIndex);
    updateFlagButton();
});

document.getElementById('skip-btn').addEventListener('click', () => {
    if (currentQuestionIndex < filteredQuizData.length - 1) {
        currentQuestionIndex++;
        showQuestion();
    } else {
        endQuiz();
    }
});

document.getElementById('next-btn').addEventListener('click', () => {
    if (!answerSelected) {
        const selectedAnswer = document.querySelector('input[name="answer"]:checked');
        if (!selectedAnswer) {
            alert("Пожалуйста, выберите ответ!");
            return;
        }
        checkAnswer(parseInt(selectedAnswer.value));
    } else {
        if (currentQuestionIndex < filteredQuizData.length - 1) {
            currentQuestionIndex++;
            showQuestion();
        } else {
            endQuiz();
        }
    }
});

// Обработчик выбора ответа
document.addEventListener('change', function(e) {
    if (e.target.name === 'answer') {
        document.getElementById('next-btn').disabled = false;
    }
});

document.addEventListener('DOMContentLoaded', startQuiz);